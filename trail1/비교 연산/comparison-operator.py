# 변수 선언, 입력
inp = input()
arr = inp.split()
a = int(arr[0])
b = int(arr[1])

# a >= b
if a >= b:
    print(1)
else:
    print(0)

# a > b
if a > b:
    print(1)
else:
    print(0)

# a <= b
if a <= b:
    print(1)
else:
    print(0)

# a < b
if a < b:
    print(1)
else:
    print(0)

# a == b
if a == b:
    print(1)
else:
    print(0)

# a != b
if a != b:
    print(1)
else:
    print(0)
